<template>
  <TLoader v-if="loading" />
  <form v-else class="max-w-md" @submit.prevent="next">
    <div v-if="step == 'name'">
      <TField
        ref="name"
        v-model="profile.name"
        label="Hi! What's your name?"
        label-position="vertical"
      />
    </div>

    <div v-if="step == 'username'">
      <TField
        ref="username"
        v-model="profile.username"
        label="Pick a username"
        trim="[^a-z0-9]+"
        label-position="vertical"
        description="There might be lot of people with the same name, so username will help others to differentiate who is who. Also link to your profile will be wedance.vip/u/username"
      />
    </div>

    <div v-if="step == 'gender'">
      <TField
        ref="gender"
        v-model="profile.gender"
        label="How do you identify?"
        label-position="vertical"
        type="buttons"
        :options="genderOptions"
      />
    </div>

    <div v-if="step == 'community'">
      <TField
        ref="community"
        v-model="profile.community"
        label="Dance community of which city would you like to join?"
        label-position="vertical"
        type="city"
      />
    </div>

    <div v-if="step == 'styles'">
      <TField
        v-model="profile.styles"
        label="Tell us what you're interested in"
        label-position="vertical"
        placeholder="Salsa, Bachata, Kizomba, etc."
        type="styles"
      />
    </div>

    <div v-if="step == 'photo'">
      <TField
        v-model="profile.photo"
        label="Last step! Upload your photo"
        label-position="vertical"
        type="photo"
      />

      <TButton
        v-if="!profile.photo"
        class="mt-4"
        type="secondary"
        @click="
          skip.photo = true
          next()
        "
        >Skip for now</TButton
      >
    </div>

    <div class="flex justify-end mt-4">
      <TButton type="primary" @click="next">Next</TButton>
    </div>
  </form>
</template>

<script>
import ls from 'local-storage'
import useAuth from '~/use/auth'

export default {
  middleware: ['auth'],
  name: 'Onboarding',
  layout: 'popup',

  setup() {
    const {
      profile: loadedProfile,
      loading: loadingAuth,
      updateProfile,
      updateAccount
    } = useAuth()

    return {
      updateProfile,
      updateAccount,
      loadingAuth,
      loadedProfile
    }
  },

  data: () => ({
    step: 'name',
    profile: {},
    skip: {},
    nextSteps: {
      name: 'username',
      username: 'gender',
      gender: 'community',
      community: 'styles',
      styles: 'photo',
      photo: 'finish'
    },
    genderOptions: [
      {
        label: 'I am a woman',
        value: 'Female'
      },
      {
        label: 'I am a man',
        value: 'Male'
      }
    ]
  }),

  computed: {
    loading() {
      return this.step === 'finish' || (this.loadingAuth && !this.loadedProfile)
    }
  },

  watch: {
    loadedProfile: 'load'
  },

  mounted() {
    const city = ls('city')

    this.profile.community = city
  },
  methods: {
    load() {
      if (this.loadedProfile && !this.profile.name) {
        this.profile = this.loadedProfile
      }
    },
    next() {
      if (this.profile[this.step] || this.skip[this.step]) {
        this.updateProfile({
          step: this.step,
          ...this.profile
        })

        this.step = this.nextSteps[this.step]

        if (this.step === 'finish') {
          this.finish()
        }
      }
    },
    async finish() {
      await this.updateAccount({
        confirmed: true
      })

      const target = `/posts/74zve0dQqtdwaSXq7RDf`

      this.$router.push(target)
    }
  }
}
</script>
